package TestProcess;

public class day1{
	public static void main(String[] args){
		/*获取两个数中较大的
		判断一个数是奇数还是偶数,并输出是奇数还是偶数*/
		int a=8;
		int b=9;
		if(a>b){
			System.out.println(a);
		}else {
			System.out.println(b);
		}

		if(a%2==0){
			System.out.println("偶数");
		}else {
			System.out.println("奇数");
		}

	}
}
